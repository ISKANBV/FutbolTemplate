function reducer(state = initState, action) {
    switch (action.type) {
        case "ADD_SIZE_THIRDKIT":
            return {
                ...state, ...state.thirdkit.map(e => {
                    if (e.id === action.payload) {
                        e.size = action.value
                        console.log(e.size);
                    }
                    return e
                })
            }

        case "ADD_SIZE_HOMEKIT":
            return {
                ...state, ...state.homekit.map(e => {
                    if (e.id === action.payload) {
                        e.size = action.value
                        console.log(e.size);
                    }
                    return e
                })
            }

        case "ADD_SIZE_AWAYKIT":
            return {
                ...state, ...state.awaykit.map(e => {
                    if (e.id === action.payload) {
                        e.size = action.value
                        console.log(e.size);
                    }
                    return e
                })
            }

        case "ADD_CART":
            return { ...state, cart: [...state.cart, action.payload] }
        case "ADD_COUNT":
            return {
                ...state, cart: [...state.cart.map(e => {
                    if (e.id === action.payload) {
                        e.count++
                    }
                    return e
                })]
            }

        case "DEL_COUNT":
            return {
                ...state, cart: [...state.cart.map(e => {
                    if (e.id === action.payload) {
                        e.count--
                    }
                    return e
                })]
            }

        case "REMOVE_CART":
            return { ...state, cart: [...state.cart.filter(a => a.id !== action.payload)] }
            case "REMOVE_ALLCART":
            return { ...state, cart: [] }


        default:
            return state;
    }
}

export default reducer